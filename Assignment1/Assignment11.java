/*11.Write a Java Program to print the area and perimeter of a circle. */
import java.lang.*;
class Assignment11
{
	public static void main(String args[])
	{
		float r=7.5F;
		float pi=3.14F;
		System.out.println("Perimeter is ="+2*pi*r);
		System.out.println("Area is ="+pi*r*r);
	}
}

